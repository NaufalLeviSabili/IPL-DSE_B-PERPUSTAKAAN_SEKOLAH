    private void table_update() {
        int CC;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/perpustakaan_sekolah","root","");
            pst1 = con.prepareStatement("SELECT * FROM pengembalian_buku");
            ResultSet Rs = pst1.executeQuery();
            ResultSetMetaData RSMD = Rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
            DFT.setRowCount(0);

            while (Rs.next()) {
                Vector v2 = new Vector();
                for (int ii = 1; ii <= CC; ii++) {
                    v2.add(Rs.getString("kode_buku"));
                    v2.add(Rs.getString("judul_buku"));
                    v2.add(Rs.getString("penulis"));
                    v2.add(Rs.getString("penerbit"));
                    v2.add(Rs.getString("tahun_terbit"));
                    v2.add(Rs.getString("nis"));
                    v2.add(Rs.getString("nama_peminjam"));
                    v2.add(Rs.getString("return_date"));
                    v2.add(Rs.getString("elp"));
                    v2.add(Rs.getString("fine"));
                }
                DFT.addRow(v2);
            }
        } catch (Exception e) {
        }
    }  
