            pst3.setString(2,judul);
            pst3.setString(3,npenulis);
            pst3.setString(4,npenerbit); 
            pst3.setString(5,thnterbits); 
            pst3.setString(6,nis); 
            pst3.setString(7,npeminjam);
            pst3.setString(8,duedate);
            pst3.setString(9,elped);
            pst3.setString(10,fines);
            pst3.executeUpdate();
            
            pst = con.prepareStatement("update daftar_buku set status = 'Tersedia' where kode_buku = ?  ");
            pst.setString(1, kode);
            pst.executeUpdate();
            
            pst2 = con.prepareStatement("Delete from peminjaman where kode_buku = ?  ");
            pst2.setString(1, kode);
            pst2.executeUpdate();            
            
            JOptionPane.showMessageDialog(this, "Data Diperbaharui!");
