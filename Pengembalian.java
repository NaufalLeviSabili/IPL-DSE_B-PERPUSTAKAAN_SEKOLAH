    private void kode_bukuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kode_bukuActionPerformed
        // TODO add your handling code here:
        try{
            String nobuk = kode_buku.getSelectedItem().toString();
                    
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/perpustakaan_sekolah", "root", "");
            pst5 = con.prepareStatement("select judul_buku,penulis,penerbit,tahun_terbit,nis,nama_peminjam,due,total_hari,DATEDIFF(NOW(),due) as elap from peminjaman where kode_buku = ? ");
            pst5.setString(1, nobuk);
            rs2 = pst5.executeQuery();
            if (rs2.next() == false){
                JOptionPane.showMessageDialog(null, "Data Tidak Tersedia");
            }else{
                String juduls = rs2.getString("judul_buku");
                jdl_buku.setText(juduls.trim());
                
                String pnuliz = rs2.getString("penulis");
                txnama_penulis.setText(pnuliz.trim());
                
                String pnerbid = rs2.getString("penerbit");
                txtpenerbit.setText(pnerbid.trim());
                
                String thntrbit = rs2.getString("tahun_terbit");
                thnterbit.setText(thntrbit.trim());
                
                String niss = rs2.getString("nis");
                txt_nis.setText(niss.trim());
                
                String nama = rs2.getString("nama_peminjam");
                txnama_peminjam.setText(nama.trim());
                
                String tgl_kml = rs2.getString("due");
                txtdue.setText(tgl_kml.trim());
                
                String elp = rs2.getString("elap");
                int elaped = Integer.parseInt(elp);
                  if(elaped>0){
                    txtelp.setText(elp);
                    int fine = elaped * 5000;
                    txtfine.setText(String.valueOf(fine));
                  }else{
                    txtelp.setText("0");
                    txtfine.setText("0");
                  }
            }
           
        }catch (ClassNotFoundException ex){
            Logger.getLogger(AnggotaPerpustakaan.class.getName()).log(Level.SEVERE, null, ex);
        }catch(SQLException ex){
            Logger.getLogger(AnggotaPerpustakaan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_kode_bukuActionPerformed

